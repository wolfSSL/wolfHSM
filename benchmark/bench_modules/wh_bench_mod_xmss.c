/* Helper function for XMSS verify benchmark. Loads the precomputed public key
 * into the HSM cache (ephemeral, verify-only) and times wc_XmssKey_Verify over
 * the fixed signature. No keygen/sign, so this runs in verify-only builds. */
static int _benchXmssVerify(whClientContext* client, whBenchOpContext* ctx,
                            int id)
{
    int          ret             = 0;
    XmssKey      key;
    const byte   msg[]           = WH_BENCH_XMSS_MSG;
    const word32 msgSz           = (word32)(sizeof(msg) - 1);
    int          i;
    int          initialized_key = 0;
    whKeyId      keyId           = WH_KEYID_ERASED;
    char         keyLabel[]      = "bench-xmss-verify-key";

    (void)wh_Client_SetDmaMode(client, 1);

    /* Initialize the XMSS key */
    ret = wc_XmssKey_Init(&key, NULL, WH_CLIENT_DEVID(client));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_XmssKey_Init %d\n", ret);
        goto exit;
    }
    initialized_key = 1;

    /* Bind the parameter string (required before importing the public key) */
    ret = wc_XmssKey_SetParamStr(&key, WH_BENCH_XMSS_PARAM_STR);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_XmssKey_SetParamStr %d\n", ret);
        goto exit;
    }

    /* Load the precomputed public key into the wolfCrypt structure */
    ret = wc_XmssKey_ImportPubRaw(&key, bench_xmss_sha2_10_256_pub,
                                  (word32)sizeof(bench_xmss_sha2_10_256_pub));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_XmssKey_ImportPubRaw %d\n", ret);
        goto exit;
    }

    /* Provision the verify-only public key into the HSM cache. EPHEMERAL keeps
     * it out of NVM; it is evicted during cleanup. */
    ret = wh_Client_XmssImportPubKey(client, &key, &keyId,
                                     WH_NVM_FLAGS_EPHEMERAL,
                                     (uint16_t)strlen(keyLabel),
                                     (uint8_t*)keyLabel);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wh_Client_XmssImportPubKey %d\n", ret);
        goto exit;
    }

    /* Set key ID */
    ret = wh_Client_XmssSetKeyId(&key, keyId);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wh_Client_XmssSetKeyId %d\n", ret);
        goto exit;
    }

    /* Benchmark the verification operation over the fixed valid signature */
    for (i = 0; i < WOLFHSM_CFG_BENCH_PK_ITERS && ret == 0; i++) {
        int benchStartRet;
        int benchStopRet;

        /* Time only the verify operation */
        benchStartRet = wh_Bench_StartOp(ctx, id);
        ret = wc_XmssKey_Verify(&key, bench_xmss_sha2_10_256_sig,
                                (word32)sizeof(bench_xmss_sha2_10_256_sig), msg,
                                (int)msgSz);
        benchStopRet = wh_Bench_StopOp(ctx, id);

        if (benchStartRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StartOp %d\n", benchStartRet);
            ret = benchStartRet;
            break;
        }
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_XmssKey_Verify %d\n", ret);
            break;
        }
        if (benchStopRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StopOp %d\n", benchStopRet);
            ret = benchStopRet;
            break;
        }
    }

exit:
    /* Clean up resources */
    if (initialized_key) {
        wc_XmssKey_Free(&key);
    }

    /* Evict the cached verify-only public key */
    if (!WH_KEYID_ISERASED(keyId)) {
        int evictRet = wh_Client_KeyEvict(client, keyId);
        if ((evictRet != 0) && (ret == 0)) {
            ret = evictRet;
        }
    }

    return ret;
}

