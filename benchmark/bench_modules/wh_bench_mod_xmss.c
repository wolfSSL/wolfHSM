/* Helper function for XMSS key generation benchmark */
static int _benchXmssKeyGen(whClientContext* client, whBenchOpContext* ctx,
                            int id)
{
    int     ret             = 0;
    XmssKey key;
    WC_RNG  rng[1]          = {0};
    int     i;
    int     initialized_rng = 0;
    int     initialized_key = 0;
    whKeyId keyId           = WH_KEYID_ERASED;

    (void)wh_Client_SetDmaMode(client, 1);

    /* Initialize the RNG */
    ret = wc_InitRng_ex(rng, NULL, WH_CLIENT_DEVID(client));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_InitRng_ex %d\n", ret);
        return ret;
    }
    initialized_rng = 1;

    /* Benchmark the key generation */
    for (i = 0; i < WOLFHSM_CFG_BENCH_KG_ITERS && ret == 0; i++) {
        int benchStartRet;
        int benchStopRet;
        int keyIdRet = 0;

        /* Initialize the XMSS key before each iteration */
        ret = wc_XmssKey_Init(&key, NULL, WH_CLIENT_DEVID(client));
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_XmssKey_Init %d\n", ret);
            break;
        }
        initialized_key = 1;

        /* Bind the XMSS parameter string */
        ret = wc_XmssKey_SetParamStr(&key, WH_BENCH_XMSS_PARAM_STR);
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_XmssKey_SetParamStr %d\n", ret);
            break;
        }

        /* Time only the key generation. Keygen commits the private key to NVM
         * on the server, so it is erased afterwards outside the timed region. */
        benchStartRet = wh_Bench_StartOp(ctx, id);
        ret           = wc_XmssKey_MakeKey(&key, rng);
        benchStopRet  = wh_Bench_StopOp(ctx, id);

        /* Capture the keyId as soon as the key exists so cleanup can erase the
         * committed key even if a check below fails. The getter only reads a
         * field, but its result gates the NVM cleanup, so check it too. */
        if (ret == 0) {
            keyIdRet = wh_Client_XmssGetKeyId(&key, &keyId);
        }

        if (benchStartRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StartOp %d\n", benchStartRet);
            ret = benchStartRet;
            break;
        }
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_XmssKey_MakeKey %d\n", ret);
            break;
        }
        if (keyIdRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Client_XmssGetKeyId %d\n", keyIdRet);
            ret = keyIdRet;
            break;
        }
        if (benchStopRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StopOp %d\n", benchStopRet);
            ret = benchStopRet;
            break;
        }

        /* Erase the committed key from cache + NVM before the next iteration */
        if (!WH_KEYID_ISERASED(keyId)) {
            int eraseRet = wh_Client_KeyErase(client, keyId);
            if (eraseRet != 0) {
                WH_BENCH_PRINTF("Failed to erase key %d\n", eraseRet);
                ret = eraseRet;
                break;
            }
            keyId = WH_KEYID_ERASED;
        }

        /* Free the key after each iteration */
        wc_XmssKey_Free(&key);
        initialized_key = 0;
    }

    /* Clean up resources */
    if (initialized_key) {
        wc_XmssKey_Free(&key);
    }
    if (initialized_rng) {
        wc_FreeRng(rng);
    }

    /* Erase a key left committed by a mid-iteration failure */
    if (!WH_KEYID_ISERASED(keyId)) {
        int eraseRet = wh_Client_KeyErase(client, keyId);
        if ((eraseRet != 0) && (ret == 0)) {
            ret = eraseRet;
        }
    }

    return ret;
}

