/*
 * Timing characteristics of these benchmarks
 * ------------------------------------------
 * XMSS is a stateful hash-based signature scheme: each signature consumes one
 * one-time key and advances the private key state. The operations behave very
 * differently under timing:
 *
 * - VERIFY is state-independent. It recomputes the WOTS+ public key candidate
 *   and walks the H authentication-path nodes to the root, a fixed amount of
 *   work regardless of the signature index. Its per-op time is flat. It runs
 *   against a fixed precomputed (public key, signature) pair, so - unlike
 *   keygen/sign - it is also available in a verify-only build, the config most
 *   likely deployed on a verifying device in the field.
 *
 * - SIGN is state-dependent, and - unlike LMS - fast relative to KEY-GEN. The
 *   server persists the full BDS traversal state, so wc_XmssKey_Reload (see
 *   _HandleXmssSignDma in wh_server_crypto.c) restores it cheaply rather than
 *   rebuilding the tree. Each sign then advances the BDS traversal to prepare
 *   the next auth path, and that cost is not uniform across indices: it spikes
 *   when treehash instances complete at subtree boundaries. Measured over
 *   consecutive indices the per-sign latency varies by several times, while
 *   KEY-GEN (which builds the whole tree once) is far slower than either.
 *
 * - XMSS has no "sign smoothing" option in wolfCrypt. The BDS worst-case-
 *   reduction parameter k (params->bds_k) is fixed to 0 for every parameter set
 *   (WC_XMSS_MAX_BDS_K == 0 in wc_xmss.h), so there is no build-time way to
 *   flatten the per-signature cost. Signing therefore stays inherently
 *   state-dependent.
 *
 * Benchmarking caveats:
 * - SIGN only exercises the first WOLFHSM_CFG_BENCH_PK_ITERS signature indices,
 *   so the reported average undersamples the more expensive authentication-path
 *   refreshes that occur deeper in the key's lifetime.
 * - KEY-GEN rebuilds the full 2^10 (1024-leaf) Merkle tree on every iteration,
 *   so it runs WOLFHSM_CFG_BENCH_KG_ITERS full tree builds (~seconds each) and
 *   dominates the wall-clock of a full benchmark run - by far the slowest module
 *   in the suite. Lower WOLFHSM_CFG_BENCH_KG_ITERS to shorten it.
 */

