/* Signature buffer sized generously for L1_H5_W8 (~1328 bytes). File scope to
 * keep it off the stack; benchmark modules run sequentially, so a single shared
 * buffer is safe. */
static byte g_lmsSigBuf[2048];

/* Helper function for LMS key generation benchmark */
static int _benchLmsKeyGen(whClientContext* client, whBenchOpContext* ctx,
                           int id)
{
    int     ret             = 0;
    LmsKey  key;
    WC_RNG  rng[1]          = {0};
    int     i;
    int     initialized_rng = 0;
    int     initialized_key = 0;
    whKeyId keyId           = WH_KEYID_ERASED;

    (void)wh_Client_SetDmaMode(client, 1);

    /* Initialize the RNG */
    ret = wc_InitRng_ex(rng, NULL, WH_CLIENT_DEVID(client));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_InitRng_ex %d\n", ret);
        return ret;
    }
    initialized_rng = 1;

    /* Benchmark the key generation */
    for (i = 0; i < WOLFHSM_CFG_BENCH_KG_ITERS && ret == 0; i++) {
        int benchStartRet;
        int benchStopRet;
        int keyIdRet = 0;

        /* Initialize the LMS key before each iteration */
        ret = wc_LmsKey_Init(&key, NULL, WH_CLIENT_DEVID(client));
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_LmsKey_Init %d\n", ret);
            break;
        }
        initialized_key = 1;

        /* Bind the LMS parameter set */
        ret = wc_LmsKey_SetParameters(&key, WH_BENCH_LMS_LEVELS,
                                      WH_BENCH_LMS_HEIGHT,
                                      WH_BENCH_LMS_WINTERNITZ);
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_LmsKey_SetParameters %d\n", ret);
            break;
        }

        /* Time only the key generation. Keygen commits the private key to NVM
         * on the server, so it is erased afterwards outside the timed region. */
        benchStartRet = wh_Bench_StartOp(ctx, id);
        ret           = wc_LmsKey_MakeKey(&key, rng);
        benchStopRet  = wh_Bench_StopOp(ctx, id);

        /* Capture the keyId as soon as the key exists so cleanup can erase the
         * committed key even if a check below fails. The getter only reads a
         * field, but its result gates the NVM cleanup, so check it too. */
        if (ret == 0) {
            keyIdRet = wh_Client_LmsGetKeyId(&key, &keyId);
        }

        if (benchStartRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StartOp %d\n", benchStartRet);
            ret = benchStartRet;
            break;
        }
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_LmsKey_MakeKey %d\n", ret);
            break;
        }
        if (keyIdRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Client_LmsGetKeyId %d\n", keyIdRet);
            ret = keyIdRet;
            break;
        }
        if (benchStopRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StopOp %d\n", benchStopRet);
            ret = benchStopRet;
            break;
        }

        /* Erase the committed key from cache + NVM before the next iteration */
        if (!WH_KEYID_ISERASED(keyId)) {
            int eraseRet = wh_Client_KeyErase(client, keyId);
            if (eraseRet != 0) {
                WH_BENCH_PRINTF("Failed to erase key %d\n", eraseRet);
                ret = eraseRet;
                break;
            }
            keyId = WH_KEYID_ERASED;
        }

        /* Free the key after each iteration */
        wc_LmsKey_Free(&key);
        initialized_key = 0;
    }

    /* Clean up resources */
    if (initialized_key) {
        wc_LmsKey_Free(&key);
    }
    if (initialized_rng) {
        wc_FreeRng(rng);
    }

    /* Erase a key left committed by a mid-iteration failure */
    if (!WH_KEYID_ISERASED(keyId)) {
        int eraseRet = wh_Client_KeyErase(client, keyId);
        if ((eraseRet != 0) && (ret == 0)) {
            ret = eraseRet;
        }
    }

    return ret;
}

/* Helper function for LMS sign benchmark */
static int _benchLmsSign(whClientContext* client, whBenchOpContext* ctx, int id)
{
    int        ret             = 0;
    LmsKey     key;
    WC_RNG     rng[1]          = {0};
    const byte msg[]           = WH_BENCH_LMS_MSG;
    const word32 msgSz         = (word32)(sizeof(msg) - 1);
    word32     sigSz           = 0;
    int        i;
    int        iters           = WOLFHSM_CFG_BENCH_PK_ITERS;
    int        initialized_rng = 0;
    int        initialized_key = 0;
    whKeyId    keyId           = WH_KEYID_ERASED;

    (void)wh_Client_SetDmaMode(client, 1);

    /* Initialize the RNG */
    ret = wc_InitRng_ex(rng, NULL, WH_CLIENT_DEVID(client));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_InitRng_ex %d\n", ret);
        goto exit;
    }
    initialized_rng = 1;

    /* Initialize the LMS key */
    ret = wc_LmsKey_Init(&key, NULL, WH_CLIENT_DEVID(client));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_LmsKey_Init %d\n", ret);
        goto exit;
    }
    initialized_key = 1;

    /* Bind the LMS parameter set */
    ret = wc_LmsKey_SetParameters(&key, WH_BENCH_LMS_LEVELS,
                                  WH_BENCH_LMS_HEIGHT, WH_BENCH_LMS_WINTERNITZ);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_LmsKey_SetParameters %d\n", ret);
        goto exit;
    }

    /* Determine the signature length and confirm it fits the buffer */
    ret = wc_LmsKey_GetSigLen(&key, &sigSz);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_LmsKey_GetSigLen %d\n", ret);
        goto exit;
    }
    if (sigSz > sizeof(g_lmsSigBuf)) {
        WH_BENCH_PRINTF("LMS sig buffer too small: need=%u have=%u\n",
                        (unsigned)sigSz, (unsigned)sizeof(g_lmsSigBuf));
        ret = BUFFER_E;
        goto exit;
    }

    /* Generate the signing key once (untimed). Keygen commits the key to NVM
     * on the server, so it is erased during cleanup. */
    ret = wc_LmsKey_MakeKey(&key, rng);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_LmsKey_MakeKey %d\n", ret);
        goto exit;
    }
    /* Capture the committed key's id so cleanup can erase it. Its result gates
     * NVM cleanup, so surface a failure rather than leaking the key. */
    ret = wh_Client_LmsGetKeyId(&key, &keyId);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wh_Client_LmsGetKeyId %d\n", ret);
        goto exit;
    }

    /* A fresh key must have signatures available */
    if (wc_LmsKey_SigsLeft(&key) == 0) {
        WH_BENCH_PRINTF("LMS reported exhausted on fresh key\n");
        ret = WH_ERROR_ABORTED;
        goto exit;
    }

    /* LMS is stateful: the key holds 2^H one-time signatures. Cap the loop to
     * that budget so raising the global WOLFHSM_CFG_BENCH_PK_ITERS (shared with
     * the other PK benchmarks) does not exhaust the key mid-run. */
    if (iters > (1 << WH_BENCH_LMS_HEIGHT)) {
        WH_BENCH_PRINTF("LMS sign capped to %d iterations (2^H OTS budget)\n",
                        (1 << WH_BENCH_LMS_HEIGHT));
        iters = (1 << WH_BENCH_LMS_HEIGHT);
    }

    /* Benchmark the signing operation. Each sign advances the private state and
     * commits it to NVM on the server before returning the signature. */
    for (i = 0; i < iters && ret == 0; i++) {
        int    benchStartRet;
        int    benchStopRet;
        word32 localSigSz = sigSz;

        /* Time only the sign operation */
        benchStartRet = wh_Bench_StartOp(ctx, id);
        ret = wc_LmsKey_Sign(&key, g_lmsSigBuf, &localSigSz, msg, (int)msgSz);
        benchStopRet = wh_Bench_StopOp(ctx, id);

        if (benchStartRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StartOp %d\n", benchStartRet);
            ret = benchStartRet;
            break;
        }
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_LmsKey_Sign %d\n", ret);
            break;
        }
        if (benchStopRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StopOp %d\n", benchStopRet);
            ret = benchStopRet;
            break;
        }
    }

exit:
    /* Clean up resources */
    if (initialized_key) {
        wc_LmsKey_Free(&key);
    }
    if (initialized_rng) {
        wc_FreeRng(rng);
    }

    /* Erase the committed key from cache + NVM */
    if (!WH_KEYID_ISERASED(keyId)) {
        int eraseRet = wh_Client_KeyErase(client, keyId);
        if ((eraseRet != 0) && (ret == 0)) {
            ret = eraseRet;
        }
    }

    return ret;
}

