/*
 * Timing characteristics of these benchmarks
 * ------------------------------------------
 * LMS is a stateful hash-based signature scheme: each signature consumes one
 * one-time key and advances the private key state. In wolfHSM the observed
 * timing is shaped less by the LMS algorithm than by how the server persists
 * the key:
 *
 * - VERIFY is state-independent. It recomputes the LM-OTS public key candidate
 *   and walks the H authentication-path nodes to the root, a fixed amount of
 *   work regardless of the signature index. Its per-op time is flat. It runs
 *   against a fixed precomputed (public key, signature) pair, so - unlike
 *   keygen/sign - it is also available in a verify-only build, the config most
 *   likely deployed on a verifying device in the field.
 *
 * - SIGN is dominated by key reconstruction, not by the signature itself. The
 *   server persists only a compact LMS private key to NVM; before each sign it
 *   calls wc_LmsKey_Reload (see _HandleLmsSignDma in wh_server_crypto.c), which
 *   rebuilds the Merkle tree state from the seed. That reload costs about as
 *   much as key generation, so SIGN throughput here tracks KEY-GEN throughput
 *   and stays essentially flat across indices. (Standalone, key-resident
 *   wolfCrypt would instead advance an in-memory auth-path traversal per sign,
 *   which is cheap but state-dependent - wolfHSM does not keep that state
 *   resident between requests.)
 *
 * - Because the key is reconstructed from scratch on every request, the
 *   wolfCrypt "sign smoothing" optimization (on by default for single-level LMS
 *   unless WOLFSSL_LMS_NO_SIGN_SMOOTHING is defined) has no observable effect in
 *   this benchmark: smoothing spreads the cost of an in-process incremental
 *   traversal across many resident signatures, and that traversal never
 *   survives between signs here. Building with WOLFSSL_LMS_NO_SIGN_SMOOTHING
 *   produces identical timings (verified).
 *
 * Benchmarking note: only the first WOLFHSM_CFG_BENCH_PK_ITERS indices are
 * signed. Because each sign reloads the full tree, per-sign cost is uniform, so
 * this small sample is representative for LMS.
 */

