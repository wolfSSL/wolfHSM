/* Helper function for LMS verify benchmark. Loads the precomputed public key
 * into the HSM cache (ephemeral, verify-only) and times wc_LmsKey_Verify over
 * the fixed signature. No keygen/sign, so this runs in verify-only builds. */
static int _benchLmsVerify(whClientContext* client, whBenchOpContext* ctx,
                           int id)
{
    int          ret             = 0;
    LmsKey       key;
    const byte   msg[]           = WH_BENCH_LMS_MSG;
    const word32 msgSz           = (word32)(sizeof(msg) - 1);
    int          i;
    int          initialized_key = 0;
    whKeyId      keyId           = WH_KEYID_ERASED;
    char         keyLabel[]      = "bench-lms-verify-key";

    (void)wh_Client_SetDmaMode(client, 1);

    /* Initialize the LMS key */
    ret = wc_LmsKey_Init(&key, NULL, WH_CLIENT_DEVID(client));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_LmsKey_Init %d\n", ret);
        goto exit;
    }
    initialized_key = 1;

    /* Bind the parameter set (required before importing the public key) */
    ret = wc_LmsKey_SetParameters(&key, WH_BENCH_LMS_LEVELS,
                                  WH_BENCH_LMS_HEIGHT, WH_BENCH_LMS_WINTERNITZ);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_LmsKey_SetParameters %d\n", ret);
        goto exit;
    }

    /* Load the precomputed public key into the wolfCrypt structure */
    ret = wc_LmsKey_ImportPubRaw(&key, bench_lms_l1h5w8_pub,
                                 (word32)sizeof(bench_lms_l1h5w8_pub));
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wc_LmsKey_ImportPubRaw %d\n", ret);
        goto exit;
    }

    /* Provision the verify-only public key into the HSM cache. EPHEMERAL keeps
     * it out of NVM; it is evicted during cleanup. */
    ret = wh_Client_LmsImportPubKey(client, &key, &keyId, WH_NVM_FLAGS_EPHEMERAL,
                                    (uint16_t)strlen(keyLabel),
                                    (uint8_t*)keyLabel);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wh_Client_LmsImportPubKey %d\n", ret);
        goto exit;
    }

    /* Set key ID */
    ret = wh_Client_LmsSetKeyId(&key, keyId);
    if (ret != 0) {
        WH_BENCH_PRINTF("Failed to wh_Client_LmsSetKeyId %d\n", ret);
        goto exit;
    }

    /* Benchmark the verification operation over the fixed valid signature */
    for (i = 0; i < WOLFHSM_CFG_BENCH_PK_ITERS && ret == 0; i++) {
        int benchStartRet;
        int benchStopRet;

        /* Time only the verify operation */
        benchStartRet = wh_Bench_StartOp(ctx, id);
        ret = wc_LmsKey_Verify(&key, bench_lms_l1h5w8_sig,
                               (word32)sizeof(bench_lms_l1h5w8_sig), msg,
                               (int)msgSz);
        benchStopRet = wh_Bench_StopOp(ctx, id);

        if (benchStartRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StartOp %d\n", benchStartRet);
            ret = benchStartRet;
            break;
        }
        if (ret != 0) {
            WH_BENCH_PRINTF("Failed to wc_LmsKey_Verify %d\n", ret);
            break;
        }
        if (benchStopRet != 0) {
            WH_BENCH_PRINTF("Failed to wh_Bench_StopOp %d\n", benchStopRet);
            ret = benchStopRet;
            break;
        }
    }

exit:
    /* Clean up resources */
    if (initialized_key) {
        wc_LmsKey_Free(&key);
    }

    /* Evict the cached verify-only public key */
    if (!WH_KEYID_ISERASED(keyId)) {
        int evictRet = wh_Client_KeyEvict(client, keyId);
        if ((evictRet != 0) && (ret == 0)) {
            ret = evictRet;
        }
    }

    return ret;
}

